function loadFavorites() {
    const savedFavorites = localStorage.getItem('naiv4Favorites');
    if (savedFavorites) {
        const favoriteIds = JSON.parse(savedFavorites);
        artistsData.forEach(artist => {
            artist.favorite = favoriteIds.includes(artist.id);
        });
    }
}

// 
function saveFavorites() {
    const favoriteIds = artistsData
        .filter(artist => artist.favorite)
        .map(artist => artist.id);
    localStorage.setItem('naiv4Favorites', JSON.stringify(favoriteIds));
}

// 
function renderArtistsWithVirtualScroll(showOnlyFavorites = false) {
    const artistsList = document.getElementById('artists-list');
    artistsList.innerHTML = '';
    
    const filteredArtists = showOnlyFavorites 
        ? artistsData.filter(artist => artist.favorite) 
        : artistsData;
    
    const heightContainer = document.createElement('div');
    const estimatedRowHeight = 400;
    heightContainer.style.height = `${filteredArtists.length * estimatedRowHeight}px`;
    heightContainer.style.position = 'relative';
    artistsList.appendChild(heightContainer);
    
    let renderedRows = new Map();
    
    function renderVisibleRows() {
        const scrollTop = artistsList.scrollTop;
        const viewportHeight = artistsList.clientHeight;
        
        const startIndex = Math.floor(scrollTop / estimatedRowHeight) - 3;
        const endIndex = Math.ceil((scrollTop + viewportHeight) / estimatedRowHeight) + 3;
        
        const validStartIndex = Math.max(0, startIndex);
        const validEndIndex = Math.min(filteredArtists.length - 1, endIndex);
        
        renderedRows.forEach((row, index) => {
            if (index < validStartIndex || index > validEndIndex) {
                row.remove();
                renderedRows.delete(index);
            }
        });
        
        for (let i = validStartIndex; i <= validEndIndex; i++) {
            if (!renderedRows.has(i)) {
                const artist = filteredArtists[i];
                const artistRow = document.createElement('div');
                artistRow.className = 'artist-row';
                
                artistRow.innerHTML = `
                    <div class="favorite-col">
                        <button class="fav-btn ${artist.favorite ? 'active' : ''}" data-id="${artist.id}">
                            <i class="fas ${artist.favorite ? 'fa-heart' : 'fa-heart-broken'}"></i>
                        </button>
                    </div>
                    <div class="number-col">
                        <span class="artist-number">${artist.id}</span>
                    </div>
                    <div class="artist-col">artist:${artist.name}</div>
                    <div class="pic-col">
                        <img src="${artist.imageUrl}" alt="${artist.name}" loading="lazy">
                    </div>
                `;
                
                artistRow.style.position = 'absolute';
                artistRow.style.top = `${i * estimatedRowHeight}px`;
                artistRow.style.width = '100%';
                artistRow.style.height = `${estimatedRowHeight}px`;
                
                heightContainer.appendChild(artistRow);
                renderedRows.set(i, artistRow);
                
                const favBtn = artistRow.querySelector('.fav-btn');
                favBtn.addEventListener('click', function() {
                    const artistId = parseInt(this.getAttribute('data-id'));
                    const artist = artistsData.find(a => a.id === artistId);
                    if (artist) {
                        artist.favorite = !artist.favorite;
                        saveFavorites();
                        
                        this.classList.toggle('active');
                        this.innerHTML = `<i class="fas ${artist.favorite ? 'fa-heart' : 'fa-heart-broken'}"></i>`;
                        
                        if (showOnlyFavorites && !artist.favorite) {
                            renderArtistsWithVirtualScroll(true);
                        }
                    }
                });
            }
        }
    }
    
    renderVisibleRows();
    
    artistsList.addEventListener('scroll', renderVisibleRows);
}

function setupBackToTopButton() {
    const backToTopButton = document.getElementById('backToTop');
    
    window.addEventListener('scroll', () => {
        if (window.pageYOffset > 300) {
            backToTopButton.classList.add('visible');
        } else {
            backToTopButton.classList.remove('visible');
        }
    });
    
    backToTopButton.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
}

function setupAnnouncement() {
    const announcementModal = document.getElementById('announcement-modal');
    const closeAnnouncementBtn = document.getElementById('close-announcement');
    
    const hasSeenAnnouncement = localStorage.getItem('hasSeenAnnouncement');
    
    if (!hasSeenAnnouncement) {
        setTimeout(() => {
            announcementModal.classList.add('active');
            document.body.style.overflow = 'hidden';
        }, 500);
    }
    
    closeAnnouncementBtn.addEventListener('click', () => {
        announcementModal.classList.remove('active');
        document.body.style.overflow = '';
        
        localStorage.setItem('hasSeenAnnouncement', 'true');
    });
    
    announcementModal.addEventListener('click', (e) => {
        if (e.target === announcementModal) {
            announcementModal.classList.remove('active');
            document.body.style.overflow = '';
            localStorage.setItem('hasSeenAnnouncement', 'true');
        }
    });
}

function initApp() {
    loadFavorites();
    renderArtistsWithVirtualScroll();
    setupBackToTopButton();
    setupAnnouncement();

    document.getElementById('show-all').addEventListener('click', function() {
        document.getElementById('show-favorites').classList.remove('active');
        this.classList.add('active');
        renderArtistsWithVirtualScroll(false);
    });

    document.getElementById('show-favorites').addEventListener('click', function() {
        document.getElementById('show-all').classList.remove('active');
        this.classList.add('active');
        renderArtistsWithVirtualScroll(true);
    });

    document.getElementById('show-announcement').addEventListener('click', function() {
        const announcementModal = document.getElementById('announcement-modal');
        announcementModal.classList.add('active');
        document.body.style.overflow = 'hidden';
    });
}

document.addEventListener('DOMContentLoaded', initApp); 